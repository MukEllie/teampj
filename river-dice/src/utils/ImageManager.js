// 캐릭터 이미지 가져오기 (캐릭터 타입과 스킨 ID 기준)
const getCharacter = (characterType = 'warrior', skinId = '101') => {
  // 캐릭터 타입을 실제 이미지 ID로 변환
  const characterMap = {
    'warrior': { '101': 101, '201': 201 },
    'thief': { '101': 301, '201': 401 },
    'mage': { '101': 501, '201': 601 }
  };
  
  const characterId = characterMap[characterType]?.[skinId] || 101;
  return imagePaths.characters[characterId] || imagePaths.characters[101];
};

