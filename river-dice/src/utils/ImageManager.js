// 카테고리와 이름으로 이미지 가져오기
const getImage = (category, name) => {
  try {
    if (imagePaths[category] && imagePaths[category][name]) {
      return imagePaths[category][name];
    }
    console.warn(`Image not found: ${category}/${name}`);
    return null;
  } catch (error) {
    console.error(`Error loading image: ${category}/${name}`, error);
    return null;
  }
};

